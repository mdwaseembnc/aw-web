.MODEL SMALL
.DATA
ARR DW 1,2,3,4,5,6,7
LEN DW ($-ARR)/2
MSG1 DB "KEY IS FOUND$"
MSG2 DB "KEY IS NOT FOUND$"
X DW 6
.CODE
MOV AX,@DATA
MOV DS,AX
MOV SI,0000H
MOV DI,LEN
ADD DI,DI
SUB DI,2
RPT :CMP SI,DI
JA KNF
MOV AX,X
MOV BX,SI
ADD BX,DI
SHR BX,1
CMP AX,ARR[BX]
JE KF
JB NEXT
MOV SI,BX
ADD SI,2
JMP RPT
NEXT: MOV DI,BX
      SUB DI,2
      JMP RPT
 KF:LEA DX,MSG1
 MOV AH,09H
 INT 21H
 JMP EXIT
 KNF:LEA DX,MSG2
 MOV AH,09H
 INT 21H
 EXIT: MOV AH,4CH
 INT 21H
 END
